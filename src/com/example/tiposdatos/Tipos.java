package com.example.tiposdatos;

public class Tipos {

    public static void main(String[] args) {

        // 1. numericos

        // 1.1 enteros (ordenados por capacidad de menos a más)
        byte variable1 = 5;
        short variable2 = 10;
        int varibale3 = 30;
        long varible4 = 100;

        // 1.2 decimales (ordenados por capacidad de menos a más)
        float variable5 = 5.5f;
        double variable6 = 10.5d;

        // 2. booleano
        boolean variable7 = false;
        boolean variable8 = true;

        // 3. texto
        char variable9 = 'a';
        String variable10 = "Lorem ipsum dolor sit amet....";
    }
}
